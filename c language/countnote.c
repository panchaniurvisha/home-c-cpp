#include<stdio.h>
int count(int n)
{
	int rup[]={2000,500,200,100,50,20,10,5,2,1};
	int a,b=0;
	for(int i=0;i<10;i++)
	{
		if(n>=rup[i]);
		{
			a=n/rup[i];//500/500=1......//25000/2000=12.....1000/500=2
			b=b+a;//0+500=0+1=1......//0+12=12....0+2=2
			printf("\n %d:%d",rup[i],b);
			n=n%rup[i];//500%500=0......//25000%2000=1000....1000%500=0
			b=b-a;//1-1=0.....//12-12=0....2-2=0
		}
	}
}
